create or replace view RAW_PRD_SAP.EP1.VW_LISTA_SOLICITUDES_PEDIDO(
	SOLICITUD_PEDIDO,
	POS_SOLICITUD_PEDIDO,
	CLASE_DOCUMENTO_SOLICITUD_PEDIDO,
	GRUPO_COMPRAS,
	SOLICITANTE,
	PEDIDO,
	POSICION_PEDIDO,
	INDICADOR_BORRADO,
	ESTRATEGIA_LIBERACION,
	INDICADOR_LIBERACION,
	GRUPO_LIBERACION,
	GRUPO_ARTICULOS,
	MATERIAL,
	TEXTO_BREVE,
	CENTRO,
	FECHA_SOLICITUD,
	FECHA_LIBERACION,
	FECHA_ENTREGA,
	CANTIDAD_SOLICITUD_PEDIDO
) as
(
SELECT 
	BANFN AS SOLICITUD_PEDIDO,
	BNFPO AS POS_SOLICITUD_PEDIDO,
	BSART AS CLASE_DOCUMENTO_SOLICITUD_PEDIDO,
    EKGRP AS GRUPO_COMPRAS,
    AFNAM AS SOLICITANTE,
    EBELN AS PEDIDO,
    EBELP AS POSICION_PEDIDO,
	LOEKZ AS INDICADOR_BORRADO,
    FRGST AS ESTRATEGIA_LIBERACION,
	FRGKZ AS INDICADOR_LIBERACION,
	FRGGR AS GRUPO_LIBERACION,
	MATKL AS GRUPO_ARTICULOS,
    MATNR AS MATERIAL,
    TXZ01 AS TEXTO_BREVE,
    WERKS AS CENTRO,
    BADAT AS FECHA_SOLICITUD,
    FRGDT AS FECHA_LIBERACION,
	--BEDAT AS FECHA_PEDIDO,
    LFDAT AS FECHA_ENTREGA,
	BSMNG AS CANTIDAD_SOLICITUD_PEDIDO
FROM EBAN
);